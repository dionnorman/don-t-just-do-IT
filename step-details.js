document.addEventListener('DOMContentLoaded', function() {
    // Create step details overlay
    const overlay = document.createElement('div');
    overlay.className = 'step-details-overlay';
    overlay.innerHTML = `
        <div class="step-details-content">
            <div class="step-details-header">
                <div class="step-header-left" style="display: flex; align-items: center;">
                    <div class="step-number-large">1</div>
                    <div class="step-title-container">
                        <h2 class="step-details-title">Step Title</h2>
                        <div class="step-details-subtitle">Step Subtitle</div>
                    </div>
                </div>
                <button class="step-details-close"><i class="fas fa-times"></i></button>
            </div>
            <div class="step-details-body">
                <div class="step-details-section step-2015">
                    <h3>2015 Core Focus</h3>
                    <div class="step-2015-content"></div>
                </div>
                <div class="step-details-section step-2025">
                    <h3>2025+ Imperative</h3>
                    <div class="step-2025-content"></div>
                </div>
            </div>
            <div class="step-trends">
                <h3>Related Emerging Trends</h3>
                <div class="trend-tags"></div>
            </div>
        </div>
    `;
    document.body.appendChild(overlay);
    
    // Add click handler to roadmap table rows
    const roadmapRows = document.querySelectorAll('.roadmap-table tbody tr');
    roadmapRows.forEach(row => {
        row.addEventListener('click', function() {
            const stepNumber = parseInt(this.querySelector('.step-number').textContent);
            showStepDetails(stepNumber);
        });
    });
    
    // Add click handler to step cards
    const stepCards = document.querySelectorAll('.step-card');
    stepCards.forEach(card => {
        card.addEventListener('click', function() {
            const stepNumber = parseInt(this.querySelector('.step-number').textContent);
            showStepDetails(stepNumber);
        });
    });
    
    // Close overlay when clicking the close button
    document.querySelector('.step-details-close').addEventListener('click', function() {
        document.querySelector('.step-details-overlay').classList.remove('active');
    });
    
    // Close overlay when clicking outside the content
    overlay.addEventListener('click', function(e) {
        if (e.target === overlay) {
            overlay.classList.remove('active');
        }
    });
    
    // Function to show step details
    function showStepDetails(stepNumber) {
        const stepData = stepsDetailData[stepNumber - 1];
        if (!stepData) return;
        
        // Update overlay content
        document.querySelector('.step-number-large').textContent = stepData.number;
        document.querySelector('.step-details-title').textContent = stepData.title;
        document.querySelector('.step-details-subtitle').textContent = stepData.subtitle;
        
        // Update 2015 content
        let content2015 = `
            <h4>${stepData.original.title}</h4>
            <p>${stepData.original.description}</p>
            <ul class="step-points">
                ${stepData.original.points.map(point => `<li>${point}</li>`).join('')}
            </ul>
        `;
        document.querySelector('.step-2015-content').innerHTML = content2015;
        
        // Update 2025 content
        let content2025 = `
            <h4>${stepData.updated.title}</h4>
            <p>${stepData.updated.description}</p>
            <ul class="step-points">
                ${stepData.updated.points.map(point => `<li>${point}</li>`).join('')}
            </ul>
        `;
        document.querySelector('.step-2025-content').innerHTML = content2025;
        
        // Update trends
        const trendsHTML = stepData.trends.map(trend => 
            `<span class="trend-tag">${trend}</span>`
        ).join('');
        document.querySelector('.trend-tags').innerHTML = trendsHTML;
        
        // Show overlay
        document.querySelector('.step-details-overlay').classList.add('active');
    }
});